	<h1 class="meta-tit">诸葛亮写给儿子的一封信，仅86字！</h1>
	<p class="meta-info">发表：<a href="/">青春励志</a> &nbsp;&nbsp; 发表日期：2016-01-13 &nbsp;&nbsp;</p>
</div>




<div class="entry">
	<p><img width="630" alt="诸葛亮写给儿子的一封信，仅86字！" src="/blog/Public/Index/img/849656160.jpg" class="aligncenter size-full wp-image-38074"></p>
<p>诸葛亮写给儿子的一封信，只用了短短八十六字，但是对于为学做人有精简而具体的忠告。一千八百多年前的智慧，在今天的科技时代，原来仍然有其参考价值。让我们一起向诸葛亮学习十堂课。</p>
<p>诸葛亮诫子书：夫君子之行，静以修身，俭以养德；非澹泊无以明志，非宁静无以致远。夫学须静也，才须学也；非学无以广才，非志无以成学。怠慢则不能励精，险躁则不能冶性。年与时驰，意与岁去，遂成枯落，多不接世。悲守穷庐，将复何及！</p>
<p><strong>第一课：宁静的力量</strong></p>
<p>静以修身、非宁静无以致远、学须静也。诸葛亮忠告孩子宁静才能够修养身心，静思反省。不能够静下来，则不可以有效的计划未来，而且学习的首要条件，就是有宁静的环境。现代人大多数终日忙碌，你是否应在忙乱中静下来，反思人生方向？</p>
<p><strong>第二课：节俭的力量</strong></p>
<p>俭以养德。诸葛亮忠告孩子要节俭，以培养自己的德行。审慎理财，量入为出，不但可以摆脱负债的困扰，更可以过着简朴生活，不会成为物质的奴隶。在鼓励消费的文明社会，你有否想过节俭的好处呢？</p>
<p><strong>第三课：计划的力量</strong></p>
<p>非澹泊无以明志、非宁静无以致远。诸葛亮忠告孩子要计划人生，不要事事讲求名利，才能够了解自己的志向，要静下来，才能够细心计划将来。面对未来，你有理想吗？你有使命感吗？你有自己的<span class="wp_keywordlink_affiliate"><a target="_blank" title="View all posts in 价值观" href="/tag/%e4%bb%b7%e5%80%bc%e8%a7%82">价值观</a></span>吗？</p>
<p><strong>第四课：学习的力量</strong></p>
<p>夫学须静也、才须学也。诸葛亮忠告孩子宁静的环境对学习大有帮助，（yispace.net）当然配合专注的平静心境，就更加事半功倍。诸葛亮不是天才论的信徒，他相信才能是学习的结果。你有否全心全力的学习？你是否相信努力才有成就？</p>
<p><strong>第五课：增值的力量</strong></p>
<p>非学无以广才、非志无以成学。诸葛亮忠告孩子要增值先要立志，不愿意努力学习，就不能够增加自己的才干。但学习的过程中，决心和毅力非常重要，因为缺乏了意志力，就会半途而废。你有否想过，一鼓作气人多，坚持到底人少的道理？</p>
<p><strong>第六课：速度的力量</strong></p>
<p>怠慢则不能励精。诸葛亮忠告孩子凡事拖延就不能够快速的掌握要点。电脑时代是速度的时代，样样事情讲求效率，想不到一千八百多年前的智慧，也一样不谋而合。快人一步，不但理想达到，你有否想过，有更多时间去修正及改善？</p>
<p><strong>第七课：性格的力量</strong></p>
<p>险躁则不能冶性。诸葛亮忠告孩子太过急躁就不能够陶冶性情。心理学家说：思想影响行为，行为影响习惯，习惯影响性格，性格影响命运。诸葛亮明白生命中要作出种种平衡，要“励精”，也要“冶性”。你要提升自己性格的品质吗？</p>
<p><strong>第八课：时间的力量</strong></p>
<p>年与时驰、意与岁去。诸葛亮忠告孩子时光飞逝，意志力又会随着时间消磨，“少壮不努力，老大徒伤悲”，时间管理是个现代人的观念，细心想一想，时间不可以被管理，每天二十四小时，不多也不少，惟有管理自己，善用每分每秒。请你想一想，你有蹉跎岁月吗？</p>
<p><strong>第九课：想像的力量</strong></p>
<p>遂成枯落、多不接世、悲守穷庐、将复何及。诸葛亮忠告孩子时光飞逝，当自己变得和世界脱节，才悲叹蹉跎岁月，也于事无补。要懂得居安思危，才能够临危不乱。想像力比知识更有力量。你有没有从大处着想，小处着手，脚踏实地，规划是人生呢？</p>
<p><strong>第十课：精简的力量</strong></p>
<p>以上诸葛亮写给儿子的一封信，只用了短短八十六字，精简地传递了具体的讯息。我相信精简的表达源于清晰的思想，精简沟通更有效果。你懂得言之有物地精简沟通吗？</p>
